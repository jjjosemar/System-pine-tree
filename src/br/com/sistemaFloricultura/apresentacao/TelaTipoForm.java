    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        
        Tipo tipo = new Tipo();
        
        try{           
            
            verificarCamposObrigatorios();
            
            if (Mensagem.msgQuestionamento("Tem certeza que deseja cadastrar esse novo tipo?") == 0) {
                
            
                tipo = extrairPnlTipo();

                tipoBO.cadastrar(tipo);
                
                Mensagem.msgInformacao("Novo tipo cadastrado com sucesso!");
                
                preencherTabelaTipos(tblTipos, tipoBO.listar());
                
                txtDescricao.setText("");
            }
            
            
        } catch (SistemaFloriculturaException e) {
            
            Mensagem.msgErro(e.getMessage());
            
        }catch(Exception ex){
            
            ex.printStackTrace();
            Mensagem.msgErro("Erro!!! Exiba a seguinte mensagem para o administrador do sistema\n"+ex.getMessage());
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

