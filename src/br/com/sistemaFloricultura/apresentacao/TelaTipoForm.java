    private void verificarCamposObrigatorios() {
        
        if(this.txtDescricao.getText().trim().equals("")){
            throw new CamposObrigatoriosException();
        }
    }
    
    private void setFormulario(String nome){
        txtDescricao.setText(nome);
    }

    private Tipo extrairPnlTipo() {
        Tipo tipo = new Tipo();
        
        tipo.setNome(this.txtDescricao.getText());
        
        return tipo;
    }
    
    private void preencherTabelaTipos(JTable tabela, List<Tipo> tipos) {
        
        DefaultTableModel tabelaModel = new TelaPrincipalForm().getModelo(tabela);
        
        new TelaPrincipalForm().deletarRowsTabela(tabelaModel);
        
        for (Tipo tipo : tipos) {
            
            Object[] dados = {tipo.getCodigo(),
                              tipo.getNome(),
            };
            
            tabelaModel.addRow(dados);
        }
    }
    
     private Tipo extraiRowSelectedTblTipos() {
        
        Tipo tipo = new Tipo();
        int linha = tblTipos.getSelectedRow();
        
        if(linha != -1){
            
            tipo.setCodigo(Integer.parseInt(tblTipos.getValueAt(linha, 0).toString()));
            tipo.setNome(tblTipos.getValueAt(linha, 1).toString());
            
        }else throw new LinhaSelectionException();
        
        return tipo;
    }
