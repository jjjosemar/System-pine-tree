    private void btnConcluirVendaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConcluirVendaActionPerformed

        try{

            verificarCamposVenda();

            if(Mensagem.msgQuestionamento("Tem certeza que deseja efetuar essa venda?") == 0){

                new Thread(){
                    public void run(){

                        List<ItemVenda> itens;
                        Venda venda;
                        int codigoVenda = -1;
                        boolean vendaCadastrada = false;

                        try{

                            estadoInicialProgressoBar();

                            venda = extrairCamposFormularioVenda();

                            vendaBO.cadastrar(venda);
                            vendaCadastrada = true;
                            codigoVenda = venda.getCodigo();

                            itens = extrairItensTblvenda(codigoVenda);

                            int incremento = 100 / (itens.size()*2), porcentagem = 0;

                            for (ItemVenda item : itens) {

                                itemBO.cadastrar(item);
                                jProgressBar.setValue(porcentagem);
                                decrementarQtdeProdutoEmEstoque(item);
                                jProgressBar.setValue(porcentagem);

                                porcentagem+=incremento;
                                
                            }
                            progressoBarCarregado();

                            Mensagem.msgInformacao("Venda efetuada com sucesso!");

                            limparCamposFormularioVenda();

                            deletarRowsTabela(getModelo(tblItensVenda));

                        } catch (SistemaFloriculturaException e) {

                            Mensagem.msgErro(e.getMessage());

                        }catch(Exception ex){

                            ex.printStackTrace();
                            Mensagem.msgErro("Erro!!! Exiba a seguinte mensagem para o administrador do sistema\n"+ex.getMessage());

                            if(vendaCadastrada){

                                try {
                                    vendaBO.excluir(codigoVenda);
                                } catch (Exception e) {
                                }
                            }
                        }
                    }

                    private void estadoInicialProgressoBar() {
                        jProgressBar.setValue(0);
                        lblEfetuandoVendas.setText("Efetuando venda...");
                        pnlBarraProgresso.setVisible(true);
                    }

                    private void progressoBarCarregado() {
                        jProgressBar.setValue(100);
                        lblEfetuandoVendas.setText("concluído");

                        try {
                            sleep(1000);
                        } catch (InterruptedException ex) {
                        }

                        pnlBarraProgresso.setVisible(false);
                    }

                    private void decrementarQtdeProdutoEmEstoque(ItemVenda item) {
                        try {
                            int qtdeAtual = produtoBO.procurarCodigo(item.getIdProduto()).getQtde();
                            produtoBO.decrementarQtde(item.getQtde(), item.getIdProduto(), qtdeAtual);
                        } catch (Exception ex) {
                            ex.printStackTrace();
                        }
                    }
                }.start();
            }
        } catch (SistemaFloriculturaException e) {
            Mensagem.msgErro(e.getMessage());

        }catch(Exception ex){

            ex.printStackTrace();
            Mensagem.msgErro("Erro!!! Exiba a seguinte mensagem para o administrador do sistema\n"+ex.getMessage());
        }

        pnlBarraProgresso.setVisible(false);
    }//GEN-LAST:event_btnConcluirVendaActionPerformed

