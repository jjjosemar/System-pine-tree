    private void preencherTabelaVendas(List<Venda> vendas) throws Exception{
        SimpleDateFormat format = new SimpleDateFormat("dd/MM/yyyy");
        
        DefaultTableModel tabelaModel = getModelo(tblVendas);
        
        deletarRowsTabela(tabelaModel);
        
        for (Venda venda : vendas) {
            
            String nomeVendedor = "Não especificado";
            
            try {
                if(venda.getCodigoVendedor() != 0){
                   nomeVendedor =  vendedorBO.buscarCodigo(venda.getCodigoVendedor()).getNome();
                }
            } catch (NenhumResultadoException e) {
            }
             
            Object[] dados = {venda.getCodigo(),
                              nomeVendedor,
                              format.format(venda.getDataVenda()),
                              venda.getValorVenda()
            };

            tabelaModel.addRow(dados);       
        }
    }

