    public static float calcularValorVenda(float desconto){
        
        DefaultTableModel tabelaModel = getModelo(tblItensVenda);
        float total = 0;
                        
        for (int i = 0; i < tabelaModel.getRowCount(); i++) {
            total += Float.parseFloat(tabelaModel.getValueAt(i, 4).toString());
        }
        
        return total - desconto;
    }
    
