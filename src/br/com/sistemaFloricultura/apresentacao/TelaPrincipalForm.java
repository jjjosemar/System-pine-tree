    private Venda extrairCamposFormularioVenda() throws Exception {
        
        Venda venda = new Venda();
        
        SimpleDateFormat format = new SimpleDateFormat("dd/MM/yyyy");
        
        try {
            
            venda.setDesconto(Float.parseFloat(txtDescontoVenda.getText().trim()));
        } catch (Exception e) {
            venda.setDesconto(0);
        }
        try {
            
            venda.setValorVenda(Float.parseFloat(txtTotalPagar.getText()));
            venda.setDataVenda(new java.sql.Date(format.parse(dtDataVenda.getText()).getTime()));
            
            if(!txtCodigoVendedorVenda.getText().equals("")){
                int codigo = Integer.parseInt(txtCodigoVendedorVenda.getText().trim());
                venda.setCodigoVendedor(codigo);
            }
            
        } catch (ParseException  ex) {
            throw new ValorInvalidoException();
        } catch(NumberFormatException ex){
            throw new ValorInvalidoException();
        }
        return venda;
    }
    
    private List<ItemVenda> extrairItensTblvenda(int codigoVenda){
       
        List<ItemVenda> items = new ArrayList<>();
        
        DefaultTableModel tabelaModel = getModelo(tblItensVenda);
                        
        for (int i = 0; i < tabelaModel.getRowCount(); i++) {
            
            ItemVenda item = new ItemVenda();
            
            item.setIdProduto(Integer.parseInt(tblItensVenda.getValueAt(i, 0).toString()));
            item.setQtde(Integer.parseInt(tblItensVenda.getValueAt(i, 2).toString()));
            item.setIdVenda(codigoVenda);
            
            items.add(item);
        }
        
        return items;
    }
    
