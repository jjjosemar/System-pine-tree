    private Usuario verificarUsuario(String login) throws Exception{
        Usuario usuario;
        
        usuario = usuarioBO.buscarUsuarioPorLogin(login);
                
        if(!usuario.getSenha().equals(criptografar(txtSenha.getText()))){
            throw new UsuarioNaoEncontradoException();    
        }    
        
        return usuario;
    }

